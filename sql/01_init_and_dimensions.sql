-- =================================================================
-- ЕТАП 1: Консолідація історичних даних (Staging View)
-- =================================================================
-- Створюємо єдине аналітичне вікно з уніфікованими полями (виключаючи персональні дані: VIN та держномер)
DROP VIEW IF EXISTS v_all_years_trends;

CREATE VIEW v_all_years_trends AS
SELECT 
    2023 AS report_year,
    PERSON,
    REG_ADDR_KOATUU,
    DEP_CODE,
    DEP,
    OPER_CODE,
    OPER_NAME,
    D_REG,
    BRAND,
    MODEL,
    MAKE_YEAR,
    COLOR,
    KIND,
    BODY,
    PURPOSE,
    FUEL,
    CAPACITY,
    OWN_WEIGHT,
    TOTAL_WEIGHT
FROM reestr_2023

UNION ALL

SELECT 
    2024 AS report_year,
    PERSON,
    REG_ADDR_KOATUU,
    DEP_CODE,
    DEP,
    OPER_CODE,
    OPER_NAME,
    D_REG,
    BRAND,
    MODEL,
    MAKE_YEAR,
    COLOR,
    KIND,
    BODY,
    PURPOSE,
    FUEL,
    CAPACITY,
    OWN_WEIGHT,
    TOTAL_WEIGHT
FROM reestr_2024

UNION ALL

SELECT 
    2025 AS report_year,
    PERSON,
    REG_ADDR_KOATUU,
    DEP_CODE,
    DEP,
    OPER_CODE,
    OPER_NAME,
    D_REG,
    BRAND,
    MODEL,
    MAKE_YEAR,
    COLOR,
    KIND,
    BODY,
    PURPOSE,
    FUEL,
    CAPACITY,
    OWN_WEIGHT,
    TOTAL_WEIGHT
FROM reestr_2025;


